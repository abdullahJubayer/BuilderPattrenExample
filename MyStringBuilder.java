public class MyStringBuilder{
	
		public String val="";

		public MyStringBuilder append(String val_){
				val+=val_;
				return this;
			}

		public String getString(){
				return val;
			}
		
		public static void main(String sr[]){
				MyStringBuilder obj=new MyStringBuilder()
						.append("Abdullah");
						System.out.println("First Print:"+obj.getString());
						obj.append("Jubayer");
				System.out.println("Second Print:"+obj.getString());
				
				MyStringBuilder obj2=new MyStringBuilder()
				.append("One \n")
				.append("Two");
				System.out.println(obj2.getString());
			}
			
	}